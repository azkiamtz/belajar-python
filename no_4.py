b = 7
c = 29
d =12

operasi = (c**b)-((d+a)*b)
print(f"({c}**{b}) - (({d}+{a})*{b}) = ", operasi)
pembagian = operasi/d
print(f"Jika {operasi} / {d} = ", pembagian)
pembagian_bulat = operasi//d
print(f"Jika {operasi} // {d} = ", pembagian_bulat)
mod = d%b
print(f"Kemudian modulus {d} dengan {b} = ", mod)